# 1 = 1 1
# 2 = 2 00 11
# 3 = 3 111 100 001
# 4 = 5 0000, 0001, 0011, 1100, 1111

def zero(n):
    a = [0] * 1000001
    a[1] = 1
    a[2] = 2

    for j in range(3, n+1):
        a[j] = (a[j-1] + a[j-2]) % 15746 # 리턴 말고 여기다 % 를 해야 성능이 좀더 좋다 낮은 숫자가 저장되기 때문에
    return a[n] 
n = int(input())
print(zero(n))

